public class ViTriKhoHang {//vi tri hang trong kho
    private int id;
    private int warehouseID; //id kho
    private String code; //ma vi tri
    private String name; // ten vi tri
    private String description;
    public ViTriKhoHang() {
    }

    public ViTriKhoHang(int id, int warehouseID, String code, String name, String description) {
        this.id = id;
        this.warehouseID = warehouseID;
        this.code = code;
        this.name = name;
        this.description = description;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getWarehouseID() {
        return warehouseID;
    }

    public void setWarehouseID(int warehouseID) {
        this.warehouseID = warehouseID;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }
    
}
